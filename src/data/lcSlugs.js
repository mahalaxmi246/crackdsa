export function getLCUrl(lcStr) {
  if (!lcStr || lcStr === 'custom') return null;
  const num = parseInt(lcStr.replace('#', ''));
  const slug = LC_SLUGS[num];
  if (slug) return `https://leetcode.com/problems/${slug}/`;
  return `https://leetcode.com/problemset/?search=${num}`;
}